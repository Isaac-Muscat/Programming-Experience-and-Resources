public class TronBlock {
  public int x, y;
  
  public TronBlock(int x, int y){
    this.y = y;
    this.x = x;
  }
  
  public void setX(int x) {
    this.x = x;
  }
  
  public void setY(int y) {
    this.y = y;
  }
  
  public int getX() {
    return x;
  }
  
  public int getY() {
    return y;
  }
}
